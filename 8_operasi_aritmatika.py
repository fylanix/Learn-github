## Operasi Aritmatika

A = 10
B = 3

# Penjumlahan
hasil = A + B
print ('Hasil penjumlahan adalah:',hasil)

# Pengurangan
hasil = A - B
print ('Hasil pengurangan adalah:',hasil)

# Perkalian
hasil = A * B
print ('Hasil pekalian adalah:',hasil)

# Pembagian
hasil = A / B
print ('Hasil pembagian adalah:',hasil)

# Modulus
hasil = A % B
print ('Modulus dari angka di atas adalah:',hasil)

# Eksponen
hasil = A ** B
print ("Eksponen dari angka di atas adalah: ",hasil)

# Floor division  (Pembagian bulat)
hasil = A // B
print ('Pembagian bulat dari angka di atas adalah:',hasil)


## Prioritas dalam penghitunga 
# 1. () : dalam kurung
# 2. * / : perkalian dan penjumlahan
# 3. + - : penjumlahan dan pengurangan
