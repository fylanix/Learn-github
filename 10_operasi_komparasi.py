# Operasi Komparasi
# <,>,<=,>=,!>, is, is not

a = 4
b = 2

# Operasi lebih dari (>)
print("===== OPERASI LEBIH DARI (>)")
hasil = a > 3
print(a,'>',3,'=',hasil)
hasil = b > 3
print(b,'>',3,'=',hasil)
hasil = a > 3
print(a,'>',b,'=',hasil)

# Operasi kurang dari (<)
print("===== OPERASI KURANG DARI (<)")
hasil = a < 3
print(a,'<',3,'=',hasil)
hasil = b < 3
print(b,'<',3,'=',hasil)
hasil = a < 3
print(a,'<',b,'=',hasil)

# Operasi lebih dari sama dengan (>=)
print("===== OPERASI LEBIH DARI SAMA DENGAN (>=)")
hasil = a >= 3
print(a,'>=',3,'=',hasil)
hasil = b >= 3
print(b,'>=',3,'=',hasil)
hasil = a >= 3
print(a,'>=',b,'=',hasil)

# Operasi kurang dari sama dengan (<=)
print("===== OPERASI KURANG DARI SAMA DENGAN (>=)")
hasil = a <= 3
print(a,'<=',3,'=',hasil)
hasil = b <= 3
print(b,'<=',3,'=',hasil)
hasil = a <= 3
print(a,'<=',b,'=',hasil)

# Operasi tidak sama dengan (!=)
print("===== OPERASI TIDAK SAMA DENGAN (!=)")
hasil = a != 3
print(a,'!=',3,'=',hasil)
hasil = b != 3
print(b,'!=',3,'=',hasil)
hasil = a != 3
print(a,'!=',b,'=',hasil)

# Operasi  sama dengan (==)
print("===== OPERASI  SAMA DENGAN (==)")
hasil = a == 3
print(a,'==',3,'=',hasil)
hasil = b == 3
print(b,'==',3,'=',hasil)
hasil = a == 3
print(a,'==',b,'=',hasil)
