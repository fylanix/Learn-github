## Tipe data biasa

# Tipe data = Integer
Tipe_int = 10
print ("Tipe: ", Tipe_int)
print ("Bertipe data: ", type(Tipe_int))

# Tipe data = Float (dengan koma)
Tipe_float = 10.2
print ("Tipe: ", Tipe_float)
print ("Bertipe data: ", type(Tipe_float))

# Tipe data = String
Tipe_string = "makan malam"
print ("Tipe: ", Tipe_string)
print ("Bertipe data: ", type(Tipe_string))

## Tipe data khusus

Tipe_complex = complex (5,7)
print ("Data: ", Tipe_complex)
print ("Bertipe data: ", type(Tipe_complex))