 ## casting = merubah satu tipe data ke tipe data lain
 # contoh = integer ke float

#integer
print ("INTEGER")
data_int = 9
print ("data = ", data_int, "type = ", type(data_int))

data_float = float (data_int)
data_str = str (data_int)
data_bool = bool (data_int) #akan false apabila nilai integer nya 0
print ("data = ", data_float, "type = ", type (data_float))
print ("data = ", data_str, "type = ", type (data_str))
print ("data = ", data_bool, "type = ", type (data_bool))

## Begitupun untuk flost ke integer
# float ke integer akan dibulatkan kebawah
# akan false apabila floatnya bernilai 0

## Sedangkan untuk String ke integer dan float maka akan terjadi error, karena huruf tidak bisa ke angka
# bisa apabila stringnya angka
# false apabila string kosong, selebihnya akan true








    
